def row(arr):
    for i in arr:
        pixel(i[0], i[1], i[2])

def pixel(cx, cy, r):
    py5.fill(0)

    cx_final = cx * 25
    cy_final = cy * 25

    for _ in range(r):
        r += 25
        py5.rect(cx_final, cy_final, r, 25)

def grid(fill):
    py5.fill(fill)

    i = 0
    t = 0
    for _ in range(25):
        i += 25
        t += 1
        py5.line(i, 0, i, 625)
        py5.line(0, i, 650, i)

        py5.fill(0)
        py5.text(t, i, 25)
        py5.text(t, 0, 25 + i)

py5.run_sketch()